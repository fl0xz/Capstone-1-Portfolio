import type { ClientGroup, MorningReport, HourlyDataPoint } from '../types';

export function generateHourlyData(): HourlyDataPoint[] {
  const now = new Date();
  const data: HourlyDataPoint[] = [];

  for (let i = 23; i >= 0; i--) {
    const hour = new Date(now.getTime() - i * 60 * 60 * 1000);
    const hourLabel = hour.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' });
    const baseRevenue = 120 + Math.sin(i * 0.4) * 80;
    const baseOrders = 8 + Math.sin(i * 0.3) * 5;
    const baseViews = 2400 + Math.sin(i * 0.5) * 1200;

    data.push({
      hour: hourLabel,
      revenue: Math.round(baseRevenue + Math.random() * 60),
      orders: Math.round(baseOrders + Math.random() * 4),
      views: Math.round(baseViews + Math.random() * 800),
    });
  }

  return data;
}

